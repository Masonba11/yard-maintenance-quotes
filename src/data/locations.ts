export const primaryLocations = locations.filter((loc) => loc.isPrimary);

export function getLocationBySlugs(
  stateSlug: string,
  citySlug: string
): Location | undefined {
  return locations.find(
    (loc) => loc.stateSlug === stateSlug && loc.citySlug === citySlug
  );
}

export function getLocationsByState(stateSlug: string): Location[] {
  return locations.filter((loc) => loc.stateSlug === stateSlug);
}

export function getLocationsByMetro(metroArea: string): Location[] {
  return locations.filter((loc) => loc.metroArea === metroArea);
}
